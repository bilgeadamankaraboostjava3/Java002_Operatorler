package com.muhammet;

public class Runner_Assignment_Gorev {

	public static void main(String[] args) {
		// = atama
		int sayi;
		sayi = 4;
		System.out.println("Sayi...: "+ sayi);
		// +=
		sayi += 2; // sayi = sayi + 10
		System.out.println("+ Sayi...: "+ sayi);
		// +,-,/,*,% => +=,/=,-=,*=,%=
		sayi -=1;
		System.out.println("- Sayi...: "+ sayi);
		sayi *=25;
		System.out.println("* Sayi...: "+ sayi);
		sayi /=5;
		System.out.println("/ Sayi...: "+ sayi);
		sayi %=3;
		System.out.println("% Sayi...: "+ sayi);
	}

}
